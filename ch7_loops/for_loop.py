for i in range(100):
    print(i)


# using break ----------------
for i in range(100):
    if i == 50:
        break           #-----------------this exists the loop when the conditions meet
    print(i)

#using continue
for i in range(100):
    if i == 50:
        continue                 #-----------------this skips the loop for that when the conditions meet. the loop continues. 
    print(i)


